//O for...of é usado para iterar sobre os elementos de uma coleção, como arrays, strings ou objetos iteráveis. 

const frutas = ["Maçã", "Banana", "Laranja"];

for (let fruta of frutas) {
  console.log(fruta);
}

//Neste exemplo, o loop for...of itera sobre os elementos do array frutas. A variável fruta contém o valor de cada elemento do array.
